package com.hush.pgp.cfb;

/**
 * Thrown if an attempt is made to decrypt PGP CFB encrypted data
 * with the wrong session key.
 * 
 * @author Brian Smith
 */
public class WrongKeyException extends RuntimeException
{
	private static final long serialVersionUID = -7980070192713495362L;

	public WrongKeyException(String message)
	{
		super(message);
	}
}
