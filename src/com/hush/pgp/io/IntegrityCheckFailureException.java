package com.hush.pgp.io;

import java.io.IOException;

/**
 * This exception is thrown if the integrity check on a 
 * message (MDC) fails, and indicates that the message may have
 * been tampered with.
 */
public class IntegrityCheckFailureException extends IOException
{
	private static final long serialVersionUID = -7041076124879886850L;

	public IntegrityCheckFailureException(String message)
	{
		super(message);
	}
}
