package com.hush.pgp.io;

import java.io.IOException;

/**
 * This exception indicates that no session keys on the
 * message were successfully decrypted after all available
 * methods had been tried.
 */
public class NoSessionKeyException extends IOException
{
	private static final long serialVersionUID = -3932082288085955571L;

	public NoSessionKeyException(String message)
	{
		super(message);
	}
}
