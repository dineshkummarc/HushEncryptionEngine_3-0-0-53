package com.hush.hee.axbridge;

public class StringHolder
{
	private String string;
	
	public StringHolder()
	{
	}
	
	public StringHolder(String string)
	{
		this.setString(string);
	}
	
	public void setString(String string)
	{
		this.string = string;
	}
	
	public String getString()
	{
		return string;
	}
}
