package com.hush.hee.axbridge;

public class CanEncryptResult
{
	private com.hush.hee.CanEncryptResult delegate;

	protected CanEncryptResult(com.hush.hee.CanEncryptResult delegate)
	{
		this.delegate = delegate;
	}
	
	public String[] getAliasesWithEncryptionMethod()
	{
		return delegate.getAliasesWithEncryptionMethod();
	}

	public String[] getAliasesWithNoEncryptionMethod()
	{
		return delegate.getAliasesWithNoEncryptionMethod();
	}

	public boolean getCanEncrypt()
	{
		return delegate.getCanEncrypt();
	}

	public String[] getDeniedAliases()
	{
		return delegate.getDeniedAliases();
	}
	
	public void setAliasesWithEncryptionMethod(
			String[] aliasesWithEncryptionMethod)
	{
		delegate.setAliasesWithEncryptionMethod(aliasesWithEncryptionMethod);
	}

	public void setAliasesWithNoEncryptionMethod(
			String[] aliasesWithNoEncryptionMethod)
	{
		delegate
				.setAliasesWithNoEncryptionMethod(aliasesWithNoEncryptionMethod);
	}

	public void setDeniedAliases(String[] deniedAliases)
	{
		delegate.setDeniedAliases(deniedAliases);
	}
}
