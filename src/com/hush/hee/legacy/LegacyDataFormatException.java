package com.hush.hee.legacy;

import java.io.IOException;

public class LegacyDataFormatException extends IOException
{

	private static final long serialVersionUID = 8705364919528065823L;

	public LegacyDataFormatException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public LegacyDataFormatException(Throwable cause)
	{
		super(cause);
	}

	public LegacyDataFormatException()
	{
		super();
	}

	public LegacyDataFormatException(String message)
	{
		super(message);
	}
	
	public static LegacyDataFormatException wrap(String message, Exception exception)
	{
		return new LegacyDataFormatException(message, exception);
	}
}
